        return view('posts.create', compact('categories'));
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $post = $request->all();
        Post::create($post);
        return redirect()
            ->route('posts.index')
            ->with('success', 'Post salvo com sucesso.');
    }

